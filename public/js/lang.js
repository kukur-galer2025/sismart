/**
 * Apply translations to all elements with [data-lang] attribute
 */
function applyLang(lang) {
    const dict = SISMART_LANG[lang] || SISMART_LANG['id'];
    document.querySelectorAll('[data-lang]').forEach(el => {
        const key = el.getAttribute('data-lang');
        if (dict[key] !== undefined) {
            if (el.tagName === 'INPUT' && el.type !== 'hidden') {
                if (el.placeholder !== undefined && el.getAttribute('data-lang-attr') === 'placeholder') {
                    el.placeholder = dict[key];
                }
            } else {
                el.innerHTML = dict[key];
            }
        }
    });
    // Also update elements with data-lang-placeholder
    document.querySelectorAll('[data-lang-placeholder]').forEach(el => {
        const key = el.getAttribute('data-lang-placeholder');
        if (dict[key] !== undefined) {
            el.placeholder = dict[key];
        }
    });
    // Update elements with data-lang-title
    document.querySelectorAll('[data-lang-title]').forEach(el => {
        const key = el.getAttribute('data-lang-title');
        if (dict[key] !== undefined) {
            el.title = dict[key];
        }
    });
}

/**
 * Get translation for a key
 */
function t(key, lang) {
    lang = lang || (localStorage.getItem('sismart-lang') || 'id');
    const dict = SISMART_LANG[lang] || SISMART_LANG['id'];
    return dict[key] || key;
}
